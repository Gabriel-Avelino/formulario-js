#Para o Python, todos os comandos são funções. A estrutura para escrever algo na tela em Python é a seguinte:
print('Hello World')

#Ao apresentar um número, não usamos aspas:
print(7)

#Para fazer uma soma, basta utilizar o operador:
print(7+4) 

#Para concatenar dois números, colocamos ambos entre aspas:
print('7'+'4')

#OBS: Na interface do IDLE, não é necessário colocar o print, pois ele é feito automaticamente:

#Caso haja a tentativa de concatenar um número e uma string usando o sinal de +, o Python retornará um erro:
#print("Olá" + 5)

#Porém, ao usar uma vírgula no lugar com sinal, é possível concatenar sem se preocupar com o tipo de variável:
print('Olá', 5)